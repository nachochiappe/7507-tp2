package fiuba.algo3.algoempires.Model.Movimiento;

public class Desplazamiento {

    private int x;
    private int y;

    public Desplazamiento(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
